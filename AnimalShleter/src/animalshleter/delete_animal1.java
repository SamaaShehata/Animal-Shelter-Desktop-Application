    private void NamesComboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NamesComboActionPerformed
        // TODO add your handling code here:
        Object obj = evt.getSource();
        if(obj==NamesCombo)
        {
            try {
                String sql = "select birth_date , gender , size , color , intake_date , adoption_state , adoption_date  from animalsshelter.pets where pet_name = '"+(NamesCombo.getSelectedItem().toString())+"'";
                Statement prep = con.createStatement();
                ResultSet rst =  prep.executeQuery(sql);//resultset contain 7 coloumns by the pet name
                
                while(rst.next())
                {//each textfield will have the value of th 10 coloumns we selected from pets table
                    birthdatePet.setText(rst.getString("birth_date"));
                    color.setText(rst.getString("color"));
                    intakeDate.setText(rst.getString("intake_date"));
                    adoptionDate.setText(rst.getString("adoption_date"));
                    genderrr = rst.getString("gender");
                    sizeee.setText(rst.getString("size"));
                    adoptionStatement = rst.getInt("adoption_state");
                }
                prep.close();
                rst.close();
            } catch (SQLException ex) {
                Logger.getLogger(delete_animal1.class.getName()).log(Level.SEVERE, null, ex);
            }
            // adoption stete previous value
        
            switch (adoptionStatement) {
                case 0:
                   notadoptedButton.setSelected(true);
                    break;
                case 1:
                   adoptedButton.setSelected(true);
                    break;
                default:
                    JOptionPane.showMessageDialog(null, "Error! No Adoption Statement Found.");
                    break;
            }
            // gender previous value
            
            switch (genderrr) {
                case "Male":
                   malebutton.setSelected(true);
                    break;
                case "Female":
                    malebutton.setSelected(true);
                    break;
                default:
                    JOptionPane.showMessageDialog(null, "Error! No Gender Found.");
                    break;
            }
        
        }

        
    }//GEN-LAST:event_NamesComboActionPerformed

