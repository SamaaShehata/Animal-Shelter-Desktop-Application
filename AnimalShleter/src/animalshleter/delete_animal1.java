    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         int pet_id = 0;
        try {                                         
            
            String sqll = "select pet_id from animalsshelter.pets where pet_name = '"+NamesCombo.getSelectedItem().toString()+"'";
            PreparedStatement stmtt = con.prepareStatement(sqll);
            ResultSet result = stmtt.executeQuery();
            while (result.next())
            {
                pet_id = result.getInt("pet_id");
            }
            stmtt.close();
            result.close();
            
            try {
                String sql ="delete from animalsshelter.pets where pet_id = "+pet_id;
                PreparedStatement stt = con.prepareStatement(sql);
                stt.executeUpdate();
                stt.close();
                JOptionPane.showMessageDialog(null, "Animal Deleted Successfully.");
                fillPetNamecombo();
            } 
            catch (SQLException ex) 
            {
                Logger.getLogger(delete_animal1.class.getName()).log(Level.SEVERE, null, ex);
            }
        } 
        catch (SQLException ex) 
        {
            Logger.getLogger(delete_animal1.class.getName()).log(Level.SEVERE, null, ex);
        }
       
    }//GEN-LAST:event_jButton1ActionPerformed
         
    
    private void fillPetNamecombo()
     {
        try {
            String sqll = "SELECT * FROM animalsshelter.pets";
            PreparedStatement prep = con.prepareStatement(sqll);
            ResultSet rst =  prep.executeQuery();
            
            while (rst.next()){
                String petsNames = rst.getString("pet_name");
                NamesCombo.addItem(petsNames);
                
            }
            prep.close();
            rst.close();
            
            }
        catch (SQLException ex) {
            Logger.getLogger(delete_animal1.class.getName()).log(Level.SEVERE, null, ex);
        }
         
     }
