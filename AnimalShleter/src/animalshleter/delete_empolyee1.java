    private void fillCombobox()
    {
        try {
            String sqll = "SELECT * FROM animalsshelter.employees_info";
            PreparedStatement prep = con.prepareStatement(sqll);
            ResultSet rst =  prep.executeQuery();
            
            while (rst.next()){
                String name = rst.getString("employee_name");
                namesCombo.addItem(name);
                
            }
            prep.close();
            rst.close();
            
            }
        catch (SQLException ex) {
            Logger.getLogger(delete_empolyee1.class.getName()).log(Level.SEVERE, null, ex);
        }
         
    }
   
