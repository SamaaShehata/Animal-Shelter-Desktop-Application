    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
                int emp_id = 0;
        try {                                         
            
            String sqll = "select employee_id from animalsshelter.employees_info where employee_name = '"+namesCombo.getSelectedItem().toString()+"'";
            PreparedStatement stmtt = con.prepareStatement(sqll);
            ResultSet result = stmtt.executeQuery();
            while (result.next())
            {
                emp_id = result.getInt("employee_id");
            }
            stmtt.close();
            result.close();
            
            try {
                String sql ="delete from animalsshelter.employees_info where employee_id = "+emp_id;
                PreparedStatement stt = con.prepareStatement(sql);
                stt.executeUpdate();
                stt.close();
                JOptionPane.showMessageDialog(null, "Employee Deleted Successfully.");
                fillCombobox();
            } 
            catch (SQLException ex) 
            {
                Logger.getLogger(delete_empolyee1.class.getName()).log(Level.SEVERE, null, ex);
            }
        } 
        catch (SQLException ex) 
        {
            Logger.getLogger(delete_empolyee1.class.getName()).log(Level.SEVERE, null, ex);
        }
   
    }//GEN-LAST:event_jButton1ActionPerformed

